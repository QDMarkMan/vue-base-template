import { loginByUserName } from '@/api/login'
import { writeToken } from '@/utils/cookie'
export default {
  name: 'login',
  data() {
    return {
      name: '',
      loading: false,
      loginPara: {
        userName: '',
        password: '',
        code: '',
        remember: true
      }
    }
  },
  methods: {
    doLogin() {
      // token 虚拟登陆
      if (this.loginPara.userName !== 'admin' || this.loginPara.password !== 'admin') return this.$message.warning('请输入正确用户！')
      if (this.loginPara.code.toUpperCase() !== 'V9AM') return this.$message.warning('请输入正确验证码')
      this.loading = true
      writeToken(new Date().getDate())
      setTimeout(() => {
        this.loading = false
        // redirect
        this.$router.push({ path: '/' })
      }, 1000)
    },
    // 通过API接口登陆
    doLoginByAPI() {
      this.loading = true
      const para = {
        username: this.loginPara.userName,
        password: this.loginPara.password
        /* loginText: "",
        password: '',
        loginType: 0 */
      }
      if (this.loginPara.code.toUpperCase() !== 'V9AM') return this.$message.warning('请输入正确验证码')
      loginByUserName(para)
        .then(result => {
          if (result.success) {
            writeToken(result.result.sessionId)
            this.$message.success('登陆成功!')
            this.$router.push({ path: '/' })
          } else {
            this.$message.error(result.message)
          }
        })
        .finally(() => {
          this.loading = false
        })
    }
  },
  created() {
    console.log('welcome')
  }
}
</script>

